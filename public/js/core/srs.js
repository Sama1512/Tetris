// Super Rotation System (SRS) キックテーブル実装
export const kickData = {
    // 回転: 0→R, R→2, 2→L, L→0 の順で定義
    I: {
        0: [[0, 0], [-2, 0], [1, 0], [-2, -1], [1, 2]],
        R: [[0, 0], [-1, 0], [2, 0], [-1, 2], [2, -1]],
        2: [[0, 0], [2, 0], [-1, 0], [2, 1], [-1, -2]],
        L: [[0, 0], [1, 0], [-2, 0], [1, -2], [-2, 1]],
    },
    default: {
        0: [[0, 0], [-1, 0], [-1, 1], [0, -2], [-1, -2]],
        R: [[0, 0], [1, 0], [1, -1], [0, 2], [1, 2]],
        2: [[0, 0], [1, 0], [1, 1], [0, -2], [1, -2]],
        L: [[0, 0], [-1, 0], [-1, -1], [0, 2], [-1, 2]],
    },
};

export function getKickTable(piece, from, to) {
    const shape = piece === 'I' ? 'I' : 'default';
    const map = { 0: '0', 1: 'R', 2: '2', 3: 'L' };
    const key = map[from] || '0';
    return kickData[shape][key];
}